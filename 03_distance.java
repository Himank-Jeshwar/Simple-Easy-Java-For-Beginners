// If Rahul on an average walks 12.75 km per day for 5 days. 
// Find the Total Distance
class Distance
{   
    public static void main(String[] args) 
    {   
        //declaration
        double avg_distance=12.75;
        int day=5;
        //calculation
        double totalDistance=avg_distance*day;
        //output
        System.out.print("Total Distance Travelled = "+totalDistance+" km");

    }
}